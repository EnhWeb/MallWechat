		public List<Cms.Model.ws_Prize> DataTableToList(DataTable dt)
		{
			List<Cms.Model.ws_Prize> modelList = new List<Cms.Model.ws_Prize>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Cms.Model.ws_Prize model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

